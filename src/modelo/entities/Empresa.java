public class Empresa {
    private int E_id;
    private String E_nome;
    private int E_nuit;
    private int E_telefone;
    private String E_cidade;
    private String E_bairro;

    public int getE_id() {
        return E_id;
    }

    public void setE_id(int E_id) {
        this.E_id = E_id;
    }

    public String getE_nome() {
        return E_nome;
    }

    public void setE_nome(String E_nome) {
        this.E_nome = E_nome;
    }

    public int getE_nuit() {
        return E_nuit;
    }

    public void setE_nuit(int E_nuit) {
        this.E_nuit = E_nuit;
    }

    public int getE_telefone() {
        return E_telefone;
    }

    public void setE_telefone(int E_telefone) {
        this.E_telefone = E_telefone;
    }

    public String getE_cidade() {
        return E_cidade;
    }

    public void setE_cidade(String E_cidade) {
        this.E_cidade = E_cidade;
    }

    public String getE_bairro() {
        return E_bairro;
    }

    public void setE_bairro(String E_bairro) {
        this.E_bairro = E_bairro;
    }
    
    
}
