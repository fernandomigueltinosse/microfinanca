    private void imprimirRecibo() throws ParseException {
        final frmAguarde spash = new frmAguarde();
       
        int mesSelecionado = txtMesRelatorio.getMonth()+1;
        String mesFormatado = String.format("%02d", mesSelecionado);
        String mesExtenso = Month.of(mesSelecionado).getDisplayName(TextStyle.FULL, locale);
        String ano = String.valueOf(txtAnoRelatorio.getYear());
        Balanco bl = BalancoDao.creditos2(mesFormatado, ano);
        spash.setVisible(true);
        Thread t = new Thread() {
            @Override
            public void run() {
                
                try {
                    Map<String, Object> parametros = new HashMap<>();
                    parametros.put("dataRelatorio",mesFormatado );
                    parametros.put("mesExtenso", mesExtenso);
                    parametros.put("ano",ano);
                    parametros.put("Totalcredtor", bl.getTotal_credito());
                   
                    JasperDesign path = JRXmlLoader.load("src/relatorios/planilha_de_venda.jrxml");
                    JasperReport report = JasperCompileManager.compileReport(path);
                    JasperPrint print = JasperFillManager.fillReport(report, parametros, conn);
                    JasperViewer.viewReport(print, false);
                } catch (JRException ex) {
                    Logger.getLogger(frmPagamentos.class.getName()).log(Level.SEVERE, null, ex);
                }
                spash.dispose();
            }
        };
        t.start();

    }
